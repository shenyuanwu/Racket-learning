def cube(num):
    "return the cube of num\num\numNumber -> Number"
    return num * num * num

def avg(a,b,c,d):
    "return the average of a,b,d\a,b,c,d\a,b,c,dNumber -> Number"
    return (a + b + c + d) / 4

def triplicate(text):
    "return the triplicate of text\text\textString -> String"
    return text + text + text

def multiplicate(text,number):
    "return the number of text\text\textString -> Number"
    return text * number
